// Comparacion
console.log(3 == "3") // Compara el valor, no el tipo
console.log(3 === "3") // Compara el valor y el tipo
console.log(5 < 3) // Menor que
console.log(5 > 3) // Mayor que
console.log(5 <= 6) // Menor o igual que
console.log(5 >= 6) // Mayor o igual que
console.log(5 == 1 && "Holi" == "Hol") // true && true -> true | true && false -> false
console.log(5 == 1 || "Holi" == "Hol") // true || false -> true | false || false -> false

// Incrementacion
let edad = 40
